function currentFileName = getMintsNameGeneral(folder,nodeID,...
                                                            target,stringIn)
        nodeDataFolder      = folder+ "/"+nodeID;
        currentFileName     = nodeDataFolder+"/"+stringIn + "_" +...
                                    nodeID+ "_" + ...
                                          target + ...
                                              ".mat";
                                          
    if ~exist(fileparts(currentFileName), 'dir')
       mkdir(fileparts(currentFileName));
    end
end
